// Ejemplo 3: Verificar la existencia de un elemento en un array
const colores = ["rojo", "azul", "verde"];
const indiceVerde = colores.indexOf("verde");
if (indiceVerde !== -1) {
    console.log("El color verde existe en el array.");
} else {
    console.log("El color verde no existe en el array.");
}
// Resultado: El color verde existe en el array